// Expose the dictionary to the global scope so other scripts (script.js)
// can read translated strings for dynamically generated UI (errors, toasts, etc.)
window.dictionary = dictionary;

function applyTranslations(lang) {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dictionary[key]) {
            const icon = el.querySelector('i');
            if (icon) {
                el.innerHTML = '';
                el.appendChild(icon);
                el.innerHTML += ' ' + dictionary[key][lang];
            } else {
                el.textContent = dictionary[key][lang];
            }
        }
    });

    // Check placeholders for inputs
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (dictionary[key]) {
            el.placeholder = dictionary[key][lang];
        }
    });

    // Check titles for tooltips
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        if (dictionary[key]) {
            el.title = dictionary[key][lang];
        }
    });

    // Options mapping specifically for selects that don't use data-i18n
    document.querySelectorAll('option[data-i18n-opt]').forEach(el => {
        const key = el.getAttribute('data-i18n-opt');
        if (dictionary[key]) {
            el.textContent = dictionary[key][lang];
        }
    });

    // data-i18n-html: use innerHTML so links inside the text survive translation
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (dictionary[key]) {
            el.innerHTML = dictionary[key][lang];
        }
    });

    // data-i18n-aria: translate aria-label attributes
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        const key = el.getAttribute('data-i18n-aria');
        if (dictionary[key]) {
            el.setAttribute('aria-label', dictionary[key][lang]);
        }
    });
}

function updateLanguage(lang) {
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    document.documentElement.setAttribute('lang', lang);
    localStorage.setItem('diacare_lang', lang);
    applyTranslations(lang);

    const langToggleBtn = document.getElementById('lang-toggle');
    if (langToggleBtn) {
        langToggleBtn.textContent = lang === 'ar' ? 'English' : 'عربي';
    }

    // Trigger custom event so specific JS logic can re-render text seamlessly
    document.dispatchEvent(new Event('languageChanged'));
}

// Initial load check
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('diacare_lang') || 'en';
    updateLanguage(savedLang);

    const langToggleBtn = document.getElementById('lang-toggle');
    if (langToggleBtn) {
        langToggleBtn.addEventListener('click', () => {
            const currentLang = document.documentElement.getAttribute('lang') || 'en';
            updateLanguage(currentLang === 'en' ? 'ar' : 'en');

            // if we are in dashboard, trigger updateDashboard to refresh dynamic text like status
            if (typeof updateDashboard === 'function') {
                updateDashboard();
            }
        });
    }
});
